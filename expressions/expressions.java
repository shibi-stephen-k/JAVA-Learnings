package expressions;

public class expressions {

	public static void main(String[] args) {
		int people = 10;
		
		people = people +1;
		System.out.println(people);
		people = people -1;
		System.out.println(people);
		people = people *2;
		System.out.println(people);
		people = people /2;
		System.out.println(people);
		people = people %3;
		System.out.println(people);

	}

}
